#include <iostream>
using namespace std;

int main()
{
    /*
        DATA TYPES

        Data types specify the kind of data
        a variable can store.

        Different data types use different
        amounts of memory and store different
        kinds of values.
    */

    /*
        INTEGER DATA TYPE

        Used to store whole numbers
        without decimal values.
    */
    int age = 22;

    /*
        FLOAT DATA TYPE

        Used to store decimal numbers.
        Float provides single precision.
    */
    float pi = 3.14;

    /*
        CHARACTER DATA TYPE

        Used to store a single character.
        Characters are written inside single quotes.
    */
    char alphabet = 'z';

    /*
        STRING DATA TYPE

        Used to store multiple characters
        or text values.

        Strings are written inside double quotes.
    */
    string name = "Adarsh";

    /*
        DOUBLE DATA TYPE

        Used to store decimal numbers
        with higher precision than float.
    */
    double shareProfit = 7123.8373;

    /*
        BOOLEAN DATA TYPE

        Used to store logical values:
        true or false.
    */
    bool isMale = true;

    /*
        In boolean values:
        1 represents true
        0 represents false
    */
    bool isMen = 1;
    bool isChild = 0;

    /*
        Boolean variable storing false.
    */
    bool isFemale = false;

    /*
        LONG LONG DATA TYPE

        Used to store very large integer values.
    */
    long long shareHoldings = 116266272;

    return 0;
}